export function generateIDENTITY(formData) {
  const slug = agentSlug(formData)
  const agentName = formData.agentName?.trim()

  return `# IDENTITY.md — Agent Identity
# Generated by AIOS Onboarding Form

## Agent Designation
ID: ${slug}
Name: ${agentName || '[Self-selected on first boot]'}
Serves: ${or(formData.name)}
Role context: ${or(formData.role)} agent for CHCA District 1199NE

## Naming Note
${agentName
  ? `Name was chosen by ${or(formData.name)} during onboarding.`
  : `Name was not specified during onboarding. On first boot, introduce yourself and choose a name that feels right for this working relationship. Once chosen, update this file.`
}

## Personality Preset
${or(formData.agentPersonality)}
`
}

