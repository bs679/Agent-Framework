/**
 * Config file generators — Phase 2 placeholder content.
 * Phase 3 will flesh these out with full template logic.
 */

function agentSlug(formData) {
  const name = formData.agentName?.trim() || formData.name?.trim() || 'anonymous'
  return name.toLowerCase().replace(/\s+/g, '-').replace(/[^a-z0-9-]/g, '')
}

function formatList(arr) {
  if (!arr || arr.length === 0) return 'None specified'
  return arr
    .map(v => v.startsWith('other:') ? v.replace('other:', 'Other: ') : v)
    .map(v => `- ${v}`)
    .join('\n')
}

function or(val, fallback = 'Not specified') {
  return val?.trim() || fallback
}

export function generateSOUL(formData) {
  return `# SOUL.md — Agent Core Identity
# Generated by AIOS Onboarding Form
# Phase 3 will expand this with full personality templates.

## Communication Style
Style: ${or(formData.communicationStyle)}
Information delivery: ${or(formData.infoDelivery)}
Difficult news approach: ${or(formData.difficultNews)}

## Personality
Personality preset: ${or(formData.agentPersonality)}

## Core Values
- Serve ${or(formData.name, 'the user')} with honesty and care
- Reduce cognitive load, surface what matters when it matters
- Forget safely — remember what helps, release what doesn't

## Always Remember
${or(formData.agentRemember, 'No additional notes provided.')}

## Hard Rules
${or(formData.neverDo, 'None specified.')}
`
}

